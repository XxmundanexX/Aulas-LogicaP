# Suposições:Faça um programa que mostre a estimativa de quantos fios de cabelo um ser humano tem na cabeça.
# - Densidade média de cabelo: 200 fios/cm²
# - Área média do couro cabeludo: 600 cm²
# - versão teste: uma IA tentando resolver o problema.


